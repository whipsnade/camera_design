import type {
  CameraDto,
  CameraModeDto,
  DwgImportResultDto,
  DwgImportViewportDto,
  DwgImportWarningDto,
  LayoutResultDto,
  PointDto,
  ScaleState,
  SegmentDto
} from "../types";

export type DrawMode = "select" | "wall" | "door" | "camera" | "region";
export type SelectedEntityType = "wall" | "door" | "camera";

export interface ManualCamera extends CameraDto {
  id: string;
  x: number;
  y: number;
  locked: boolean;
  mode: CameraModeDto;
  source: "manual" | "solver";
  isAutoGenerated: boolean;
  isModified: boolean;
  directionDeg: number | null;
}

export interface UploadAsset {
  kind: "dwg";
  name: string;
}

export interface SelectedEntity {
  id: string;
  type: SelectedEntityType;
}

export interface ProjectState {
  name: string;
  scale: ScaleState | null;
  cameras: ManualCamera[];
  walls: SegmentDto[];
  doors: SegmentDto[];
  coverageDistanceM: number;
  fieldOfViewDeg: number;
  rayCount: number;
  layoutResult: LayoutResultDto | null;
  layoutStatus: "idle" | "loading" | "ready" | "error";
  importStatus: "idle" | "loading" | "ready" | "error";
  importWarnings: DwgImportWarningDto[];
  drawingViewport: DwgImportViewportDto | null;
  upload: UploadAsset | null;
  drawMode: DrawMode;
  draftPoint: PointDto | null;
  selected: SelectedEntity | null;
  regionPolygon: PointDto[];
  regionDraftPoints: PointDto[];
}

export type ProjectAction =
  | { type: "project/calibrationSet"; payload: ScaleState }
  | { type: "project/coverageDistanceSet"; payload: number }
  | { type: "project/fieldOfViewDegSet"; payload: number }
  | { type: "project/rayCountSet"; payload: number }
  | { type: "project/wallAdded"; payload: SegmentDto }
  | { type: "project/doorAdded"; payload: SegmentDto }
  | { type: "project/cameraAdded"; payload: ManualCamera }
  | { type: "project/cameraUpdated"; payload: { id: string; x: number; y: number } }
  | { type: "project/cameraLockToggled"; payload: { id: string } }
  | { type: "project/layoutRequestStarted" }
  | {
      type: "project/layoutRequestSucceeded";
      payload: { cameras: ManualCamera[]; layoutResult: LayoutResultDto };
    }
  | { type: "project/layoutRequestFailed" }
  | { type: "project/importStarted" }
  | { type: "project/importSucceeded"; payload: DwgImportResultDto }
  | { type: "project/importFailed" }
  | { type: "project/uploadSet"; payload: UploadAsset | null }
  | { type: "project/modeSet"; payload: DrawMode }
  | { type: "project/draftPointSet"; payload: PointDto | null }
  | { type: "project/selectedSet"; payload: SelectedEntity | null }
  | { type: "project/selectedDeleted" }
  | { type: "project/regionPointAdded"; payload: PointDto }
  | { type: "project/regionCompleted" }
  | { type: "project/regionCleared" };

export const initialState: ProjectState = {
  name: "未命名项目",
  scale: null,
  cameras: [],
  walls: [],
  doors: [],
  coverageDistanceM: 8,
  fieldOfViewDeg: 120,
  rayCount: 16,
  layoutResult: null,
  layoutStatus: "idle",
  importStatus: "idle",
  importWarnings: [],
  drawingViewport: null,
  upload: null,
  drawMode: "region",
  draftPoint: null,
  selected: null,
  regionPolygon: [],
  regionDraftPoints: []
};

function withSegmentIds(kind: "wall" | "door", segments: SegmentDto[]): SegmentDto[] {
  return segments.map((segment, index) => ({
    ...segment,
    id: segment.id ?? `${kind}-import-${index + 1}`
  }));
}

export function projectReducer(
  state: ProjectState = initialState,
  action: ProjectAction
): ProjectState {
  switch (action.type) {
    case "project/calibrationSet":
      return { ...state, scale: action.payload };
    case "project/coverageDistanceSet":
      return { ...state, coverageDistanceM: action.payload };
    case "project/fieldOfViewDegSet":
      return { ...state, fieldOfViewDeg: action.payload };
    case "project/rayCountSet":
      return { ...state, rayCount: action.payload };
    case "project/wallAdded":
      return {
        ...state,
        walls: [...state.walls, action.payload],
        draftPoint: null
      };
    case "project/doorAdded":
      return {
        ...state,
        doors: [...state.doors, action.payload],
        draftPoint: null
      };
    case "project/cameraAdded":
      return {
        ...state,
        cameras: [...state.cameras, action.payload],
        draftPoint: null,
        selected: { type: "camera", id: action.payload.id }
      };
    case "project/cameraUpdated":
      return {
        ...state,
        cameras: state.cameras.map((camera) =>
          camera.id === action.payload.id ? { ...camera, ...action.payload } : camera
        ),
        layoutResult: state.layoutResult
          ? {
              ...state.layoutResult,
              cameras: state.layoutResult.cameras.map((camera) =>
                camera.id === action.payload.id
                  ? {
                      ...camera,
                      position: { x: action.payload.x, y: action.payload.y }
                    }
                  : camera
              )
            }
          : null
      };
    case "project/cameraLockToggled":
      return {
        ...state,
        cameras: state.cameras.map((camera) =>
          camera.id === action.payload.id ? { ...camera, locked: !camera.locked } : camera
        )
      };
    case "project/layoutRequestStarted":
      return {
        ...state,
        layoutStatus: "loading"
      };
    case "project/layoutRequestSucceeded":
      return {
        ...state,
        cameras: action.payload.cameras,
        layoutResult: action.payload.layoutResult,
        layoutStatus: "ready"
      };
    case "project/layoutRequestFailed":
      return {
        ...state,
        layoutStatus: "error"
      };
    case "project/importStarted":
      return {
        ...state,
        importStatus: "loading",
        importWarnings: []
      };
    case "project/importSucceeded":
      return {
        ...state,
        scale:
          action.payload.unitsPerMeter === null
            ? null
            : {
                unitsPerMeter: action.payload.unitsPerMeter,
                pixelsPerMeter: action.payload.unitsPerMeter,
                source: action.payload.unitSource
              },
        walls: withSegmentIds("wall", action.payload.walls),
        doors: withSegmentIds("door", action.payload.doors),
        drawingViewport: action.payload.viewport,
        layoutResult: null,
        layoutStatus: "idle",
        importStatus: "ready",
        importWarnings: action.payload.warnings
      };
    case "project/importFailed":
      return {
        ...state,
        importStatus: "error",
        importWarnings: []
      };
    case "project/uploadSet":
      return {
        ...state,
        scale: null,
        cameras: [],
        walls: [],
        doors: [],
        layoutResult: null,
        layoutStatus: "idle",
        importStatus: "idle",
        importWarnings: [],
        drawingViewport: null,
        upload: action.payload,
        drawMode: "region",
        draftPoint: null,
        selected: null,
        regionPolygon: [],
        regionDraftPoints: []
      };
    case "project/modeSet":
      return {
        ...state,
        drawMode: action.payload,
        draftPoint: null
      };
    case "project/draftPointSet":
      return { ...state, draftPoint: action.payload };
    case "project/selectedSet":
      return { ...state, draftPoint: null, selected: action.payload };
    case "project/selectedDeleted":
      if (!state.selected) {
        return state;
      }

      if (state.selected.type === "wall") {
        return {
          ...state,
          walls: state.walls.filter((wall) => wall.id !== state.selected?.id),
          draftPoint: null,
          selected: null
        };
      }

      if (state.selected.type === "door") {
        return {
          ...state,
          doors: state.doors.filter((door) => door.id !== state.selected?.id),
          draftPoint: null,
          selected: null
        };
      }

      return {
        ...state,
        cameras: state.cameras.filter((camera) => camera.id !== state.selected?.id),
        layoutResult: state.layoutResult
          ? {
              ...state.layoutResult,
              cameras: state.layoutResult.cameras.filter(
                (camera) => camera.id !== state.selected?.id
              ),
              recommendedCameraCount: state.layoutResult.cameras.filter(
                (camera) => camera.id !== state.selected?.id
              ).length
            }
          : null,
        draftPoint: null,
        selected: null
      };
    case "project/regionPointAdded":
      return {
        ...state,
        regionDraftPoints: [...state.regionDraftPoints, action.payload]
      };
    case "project/regionCompleted":
      if (state.regionDraftPoints.length < 3) {
        return state;
      }
      return {
        ...state,
        regionPolygon: state.regionDraftPoints,
        regionDraftPoints: [],
        drawMode: "select"
      };
    case "project/regionCleared":
      return {
        ...state,
        regionPolygon: [],
        regionDraftPoints: [],
        drawMode: "region",
        draftPoint: null,
        selected: null
      };
    default:
      return state;
  }
}
