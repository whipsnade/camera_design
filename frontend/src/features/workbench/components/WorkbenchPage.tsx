import { useReducer, useRef, useState, type Dispatch } from "react";

import { exportProjectBundle, importDwgPlan, solveLayout } from "../../../api/client";
import { ControlsPanel } from "./ControlsPanel";
import { InspectorPanel } from "./InspectorPanel";
import { PlanCanvas } from "./PlanCanvas";
import { ResultsSummary } from "./ResultsSummary";
import { UploadPanel } from "./UploadPanel";
import { exportPlanImage } from "../lib/exportPlan";
import {
  initialState,
  projectReducer,
  type DrawMode,
  type ManualCamera,
  type ProjectAction,
  type ProjectState
} from "../state/projectReducer";
import type {
  ExportBundleDto,
  LayoutCameraDto,
  LayoutResultDto,
  PointDto,
  ProjectDto,
  SegmentDto
} from "../types";

const CANVAS_WIDTH = 960;
const CANVAS_HEIGHT = 520;

function makeSegment(kind: "wall" | "door", start: PointDto, end: PointDto): SegmentDto {
  return {
    id: `${kind}-${crypto.randomUUID()}`,
    start,
    end
  };
}

function makeCamera(point: PointDto): ManualCamera {
  return {
    id: `camera-${crypto.randomUUID()}`,
    x: point.x,
    y: point.y,
    locked: false,
    mode: "directional",
    source: "manual",
    isAutoGenerated: false,
    isModified: false,
    directionDeg: null
  };
}

function getSelectedSegment(state: ProjectState, kind: "wall" | "door") {
  if (!state.selected || state.selected.type !== kind) {
    return null;
  }

  const source = kind === "wall" ? state.walls : state.doors;
  return source.find((segment) => segment.id === state.selected?.id) ?? null;
}

function dispatchSegment(
  dispatch: Dispatch<ProjectAction>,
  mode: DrawMode,
  start: PointDto,
  end: PointDto
) {
  if (mode === "wall") {
    dispatch({ type: "project/wallAdded", payload: makeSegment("wall", start, end) });
    return;
  }

  if (mode === "door") {
    dispatch({ type: "project/doorAdded", payload: makeSegment("door", start, end) });
  }
}


function toSolverCamera(camera: LayoutCameraDto): ManualCamera {
  return {
    id: camera.id,
    x: camera.position.x,
    y: camera.position.y,
    locked: camera.locked,
    mode: camera.mode,
    source: "solver",
    isAutoGenerated: camera.isAutoGenerated,
    isModified: camera.isModified,
    directionDeg: camera.directionDeg
  };
}

function mergeLockedLayout(
  state: ProjectState,
  result: LayoutResultDto
): { cameras: ManualCamera[]; layoutResult: LayoutResultDto } {
  const lockedCameras = state.cameras.filter((camera) => camera.locked);
  const lockedIds = new Set(lockedCameras.map((camera) => camera.id));

  // Filter out locked cameras from the solver result since they're already in state
  const solverCameras = result.cameras.filter((camera) => !lockedIds.has(camera.id));

  return {
    cameras: [
      ...lockedCameras,
      ...solverCameras.map(toSolverCamera)
    ],
    layoutResult: {
      ...result,
      recommendedCameraCount: lockedCameras.length + solverCameras.length,
      cameras: result.cameras  // Backend returns combined result with locked cameras
    }
  };
}

export function WorkbenchPage() {
  const [state, dispatch] = useReducer(projectReducer, initialState);
  const [exportStatus, setExportStatus] = useState<"idle" | "exporting" | "done" | "error">("idle");
  const [exportBundle, setExportBundle] = useState<ExportBundleDto | null>(null);
  const [importMessage, setImportMessage] = useState<string | null>(null);
  const projectIdRef = useRef(`project-${crypto.randomUUID()}`);

  const hasRegionPolygon = state.regionPolygon.length >= 3;

  const selectedCamera =
    state.selected?.type === "camera"
      ? state.cameras.find((camera) => camera.id === state.selected?.id) ?? null
      : null;
  const selectedWall = getSelectedSegment(state, "wall");
  const selectedDoor = getSelectedSegment(state, "door");

  const projectPayload: ProjectDto = {
    id: projectIdRef.current,
    name: state.name,
    scale: state.scale,
    cameras: state.cameras.map((camera, index) => ({
      ...camera,
      label: `CAM-${String(index + 1).padStart(2, "0")}`
    })),
    walls: state.walls,
    doors: state.doors
  };

  return (
    <main className="workbench-layout" aria-label="workbench layout">
      <aside className="workbench-panel workbench-panel--sidebar">
        导入 DWG
        <UploadPanel
          importStatus={state.importStatus}
          importMessage={importMessage}
          importWarningCount={state.importWarnings.length}
          onUpload={async (file) => {
            setImportMessage(null);
            dispatch({
              type: "project/uploadSet",
              payload: {
                kind: "dwg",
                name: file.name
              }
            });
            dispatch({ type: "project/importStarted" });
            try {
              const importResult = await importDwgPlan(file);
              dispatch({
                type: "project/importSucceeded",
                payload: importResult
              });
              setImportMessage(null);
            } catch (error) {
              setImportMessage(error instanceof Error ? error.message : "DWG 导入失败");
              dispatch({ type: "project/importFailed" });
            }
          }}
          upload={state.upload}
        />
        <ControlsPanel
          activeMode={state.drawMode}
          coverageDistanceM={state.coverageDistanceM}
          fieldOfViewDeg={state.fieldOfViewDeg}
          rayCount={state.rayCount}
          hasSelection={Boolean(state.selected)}
          hasRegionPolygon={hasRegionPolygon}
          regionDraftPointCount={state.regionDraftPoints.length}
          layoutStatus={state.layoutStatus}
          onCoverageDistanceChange={(value) =>
            dispatch({ type: "project/coverageDistanceSet", payload: value })
          }
          onDeleteSelected={() => dispatch({ type: "project/selectedDeleted" })}
          onModeChange={(mode) => dispatch({ type: "project/modeSet", payload: mode })}
          onConfirmRegion={() => dispatch({ type: "project/regionCompleted" })}
          onClearRegion={() => dispatch({ type: "project/regionCleared" })}
          onRecalculate={async () => {
            const activeScale = state.scale;
            if (!activeScale) {
              return;
            }

            if ((activeScale.unitsPerMeter ?? activeScale.pixelsPerMeter ?? null) === null) {
              return;
            }

            dispatch({ type: "project/layoutRequestStarted" });

            try {
              const lockedCameras = state.cameras
                .filter((camera) => camera.locked)
                .map((camera) => ({
                  id: camera.id,
                  mode: camera.mode,
                  position: { x: camera.x, y: camera.y },
                  directionDeg: camera.directionDeg,
                  coveragePolygon: [],
                  isAutoGenerated: camera.isAutoGenerated,
                  isModified: camera.isModified,
                  locked: camera.locked
                }));

              const result = await solveLayout({
                scale: activeScale,
                coverageDistanceM: state.coverageDistanceM,
                cameraModes: ["directional", "panoramic"],
                walls: state.walls,
                doors: state.doors,
                regionPolygon: state.regionPolygon,
                lockedCameras,
                fieldOfViewDeg: state.fieldOfViewDeg,
                rayCount: state.rayCount
              });
              const merged = mergeLockedLayout(state, result);

              dispatch({
                type: "project/layoutRequestSucceeded",
                payload: merged
              });
            } catch {
              dispatch({ type: "project/layoutRequestFailed" });
            }
          }}
          onScaleChange={(unitsPerMeter) =>
            dispatch({
              type: "project/calibrationSet",
              payload: { unitsPerMeter, pixelsPerMeter: unitsPerMeter, source: "manual" }
            })
          }
          onFieldOfViewChange={(value) =>
            dispatch({ type: "project/fieldOfViewDegSet", payload: value })
          }
          onRayCountChange={(value) =>
            dispatch({ type: "project/rayCountSet", payload: value })
          }
          unitsPerMeter={state.scale?.unitsPerMeter ?? state.scale?.pixelsPerMeter ?? null}
        />
      </aside>
      <section className="workbench-panel workbench-panel--canvas">
        DWG 画布
        <PlanCanvas
          cameras={state.cameras}
          doors={state.doors}
          draftPoint={state.draftPoint}
          layoutResult={state.layoutResult}
          regionPolygon={state.regionPolygon}
          regionDraftPoints={state.regionDraftPoints}
          onCanvasClick={(point) => {
            if (state.drawMode === "region") {
              if (state.regionDraftPoints.length >= 3) {
                const first = state.regionDraftPoints[0];
                const dist = Math.hypot(point.x - first.x, point.y - first.y);
                if (dist < 20) {
                  dispatch({ type: "project/regionCompleted" });
                  return;
                }
              }
              dispatch({ type: "project/regionPointAdded", payload: point });
              return;
            }

            if (state.drawMode === "camera") {
              dispatch({
                type: "project/cameraAdded",
                payload: makeCamera(point)
              });
              return;
            }

            if (state.drawMode === "select") {
              dispatch({ type: "project/selectedSet", payload: null });
              return;
            }

            if (!state.draftPoint) {
              dispatch({ type: "project/draftPointSet", payload: point });
              return;
            }

            dispatchSegment(dispatch, state.drawMode, state.draftPoint, point);
          }}
          onSelect={(selection) => dispatch({ type: "project/selectedSet", payload: selection })}
          selected={state.selected}
          viewport={state.drawingViewport}
          walls={state.walls}
        />
      </section>
      <aside className="workbench-panel workbench-panel--sidebar">
        覆盖距离
        <ResultsSummary
          cameraCount={state.cameras.length}
          coverageRatio={state.layoutResult?.coverageRatio ?? null}
          doorCount={state.doors.length}
          exportBundle={exportBundle}
          exportStatus={exportStatus}
          overlapHintCount={state.layoutResult?.overlapHints.length ?? 0}
          onExport={async () => {
            setExportStatus("exporting");
            setExportBundle(null);

            try {
              let annotatedPngBlob: Blob | undefined;
              try {
                annotatedPngBlob = await exportPlanImage({
                  viewport: state.drawingViewport,
                  walls: state.walls,
                  doors: state.doors,
                  cameras: state.cameras,
                  layoutResult: state.layoutResult,
                  width: CANVAS_WIDTH,
                  height: CANVAS_HEIGHT
                });
              } catch {
                annotatedPngBlob = undefined;
              }

              const bundle = await exportProjectBundle({
                projectId: projectPayload.id,
                metadataJson: JSON.stringify(projectPayload),
                annotatedPngBlob
              });
              setExportBundle(bundle);
              setExportStatus("done");
            } catch {
              setExportStatus("error");
            }
          }}
          unitsPerMeter={state.scale?.unitsPerMeter ?? state.scale?.pixelsPerMeter ?? null}
          recommendedCameraCount={state.layoutResult?.recommendedCameraCount ?? null}
          importWarningCount={state.importWarnings.length}
          blindSpotCount={state.layoutResult?.blindSpots.length ?? 0}
          wallCount={state.walls.length}
        />
        <InspectorPanel
          camera={selectedCamera}
          door={selectedDoor}
          onCameraChange={(next) => {
            if (!selectedCamera) {
              return;
            }

            dispatch({
              type: "project/cameraUpdated",
              payload: { id: selectedCamera.id, x: next.x, y: next.y }
            });
          }}
          onCameraLockToggle={() => {
            if (!selectedCamera) {
              return;
            }

            dispatch({
              type: "project/cameraLockToggled",
              payload: { id: selectedCamera.id }
            });
          }}
          selected={state.selected}
          wall={selectedWall}
        />
      </aside>
    </main>
  );
}
