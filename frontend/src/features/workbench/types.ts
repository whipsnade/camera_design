export interface PointDto {
  x: number;
  y: number;
}

export interface ScaleState {
  unitsPerMeter?: number | null;
  pixelsPerMeter?: number | null;
  source: string;
}

export interface SegmentDto {
  id?: string;
  start: PointDto;
  end: PointDto;
  [key: string]: unknown;
}

export interface CameraDto {
  id?: string;
  isAutoGenerated?: boolean;
  isModified?: boolean;
  locked?: boolean;
  [key: string]: unknown;
}

export type CameraModeDto = "directional" | "panoramic";

export interface LayoutCameraDto {
  id: string;
  mode: CameraModeDto;
  position: PointDto;
  directionDeg: number | null;
  coveragePolygon: PointDto[];
  isAutoGenerated: boolean;
  isModified: boolean;
  locked: boolean;
}

export interface LayoutResultDto {
  recommendedCameraCount: number;
  coverageRatio: number;
  blindSpots: PointDto[];
  overlapHints: PointDto[];
  cameras: LayoutCameraDto[];
}

export interface LayoutSolveRequestDto {
  scale: ScaleState;
  coverageDistanceM: number;
  cameraModes: CameraModeDto[];
  walls: SegmentDto[];
  doors: SegmentDto[];
  regionPolygon: PointDto[];
  lockedCameras?: LayoutCameraDto[];
  fieldOfViewDeg?: number;
  rayCount?: number;
}

export interface ProjectDto {
  id: string;
  name: string;
  scale: ScaleState | null;
  cameras: CameraDto[];
  walls: SegmentDto[];
  doors: SegmentDto[];
}

export type ProjectCreateDto = Omit<ProjectDto, "id">;

export interface ExportBundleDto {
  pngPath: string;
  pdfPath: string;
  projectPath: string;
}

export interface RecognitionConfidenceItemDto {
  id: string;
  message: string;
  severity: string;
}

export interface RecognitionResultDto {
  scale: ScaleState | null;
  walls: SegmentDto[];
  doors: SegmentDto[];
  confidenceItems: RecognitionConfidenceItemDto[];
}

export interface DwgImportWarningDto {
  id: string;
  message: string;
  severity: string;
  sourcePath?: string;
  sourceType?: string;
}

export interface DwgImportViewportDto {
  minX: number;
  minY: number;
  maxX: number;
  maxY: number;
  width: number;
  height: number;
}

export interface DwgImportResultDto {
  unitsPerMeter: number | null;
  unitSource: string;
  viewport: DwgImportViewportDto;
  walls: SegmentDto[];
  doors: SegmentDto[];
  warnings: DwgImportWarningDto[];
}
